package model;

/**
 *
 * @author ACER-PC
 */
public class Account {
    private String mail;
    private String name;
    private String  password;
    private int role;
    private int id;

    public Account() {
    }

    public Account(String mail, String name, String password, int role, int id) {
        this.mail = mail;
        this.name = name;
        this.password = password;
        this.role = role;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

 

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }
    
}
